function Trafficlight(lampu) {
    if (lampu == "red") {
        console.log("Stop");
    }
    else if (lampu == "yellow") {
        console.log("Be Carefull!");
    }
    else if (lampu == "green") {
        console.log("Go!");
    }
}

Trafficlight("red");
Trafficlight("yellow");
Trafficlight("green");
